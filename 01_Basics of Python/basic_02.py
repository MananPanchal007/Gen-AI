# Simple If-Else
age = 20

if age >= 18:
    print("You are an adult.")
else:
    print("You are a minor.")

# If-Elif-Else Ladder
marks = 85

if marks >= 90:
    print("Grade A")
elif marks >= 75:
    print("Grade B")
elif marks >= 60:
    print("Grade C")
else:
    print("Grade D or Fail")

