# List (Dynamic Array)
fruits = ["apple", "banana", "cherry"]
print(fruits[0])  # apple
fruits.append("orange")
print(fruits)

# Tuple (Immutable List)
coordinates = (4, 5)
print(coordinates[0])

# Set (Unique Elements)
my_set = {1, 2, 2, 3}
print(my_set)  # {1,2,3}

# Dictionary (Key-Value Pairs)
person = {
    "name": "John",
    "age": 30,
    "city": "New York"
}
print(person["name"])
